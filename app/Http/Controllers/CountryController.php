     */
    public function store(Request $request)
    {
        //
        $this->validate($request, ['dateDeparture'=>'required', 'dateArrival' => 'required']);
        $numPeople = $request->input('numPeople');
        $idSelectedCountry = $request->input('listCountries'); 
        $SelectedCountry = Country::find($idSelectedCountry);   
        return view('Locations.index')->with('countrylocations', $SelectedCountry->Locations)->with('numPeople',$numPeople);
    }

